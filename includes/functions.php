<?php

    $result = array();

    function getAllItems($conn) {
        $query = "SELECT * FROM tbl_items";

        $runQuery = $conn->query($query);

        while($row = $runQuery->fetchAll(PDO::FETCH_ASSOC)) {
            $result[] = $row;
        }

        //return $result;
        echo (json_encode($result));
    }

    function getSingleItem($conn, $id) {
        $query = "SELECT * FROM tbl_items WHERE item_id=" . $id . "";

        $runQuery = $conn->query($query);

        while($row = $runQuery->fetchAll(PDO::FETCH_ASSOC)) {
            $result[] = $row;
        }

        //return $result;
        echo (json_encode($result));
    }

    function getItemFromCategory($conn, $category) {
        $query = "SELECT * FROM tbl_items WHERE item_category='" . $category . "'";

        $runQuery = $conn->query($query);

        while($row = $runQuery->fetchAll(PDO::FETCH_ASSOC)) {
            $result[] = $row;
        }

        //return $result;
        echo (json_encode($result));
    }